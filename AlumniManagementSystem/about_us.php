<p class="p2"> INTRODUCTION TO THE SAMBALPUR UNIVERSITY INSTITUTE OF INFORMATION TECHNOLOGY(SUIIT) </p>
<p class="p3">Sambalpur University Institute of Information Technology (SUIIT) is functioning successfully since the year 2010 as an autonomous constituent institute of Sambalpur University in the line of IIITs to impart training and to have quality research programme relating to Information Technology and allied subjects..</p><br />
<p class="p2">GOVERNANCE OF STUDIES</p>
<p class="p3">The Government of Odisha has provided funds to the tune of Rs. 10 crore for construction of Academic building which has been completed and was inaugurated by Shri Naveen Patnaik, Chief Minister of Odisha on January 29, 2014 .
The training in the institute has been envisioned to be holistic academic programme which includes training in humanities, management sciences and basic sciences besides the relevant technical subjects.

SUIIT is a unique venture to make use of the decades of rich academic experience and achievements of the University in diverse fields for nurturing the young minds in the contemporary globalized environment where the demand is for innovation, research and technical skill of international standard.The Director of SUIIT leads the administrative processes relating to graduate and under-graduate studies in institude. He reports to the Vice-Chancellor.  . The Sub Committee oversees the admission, appointment of supervisors and the assessment of graduate candidates.  For routine administrative processes, the Dean of SUIIT is assisted by Graduate Studies Management Committee that is chaired by the dean and comprise of deputy deans whom are responsible for graduate studies in their respective faculties.</p><br />
