<p class="p1">FAQ</p>
<p class="p2"><br>Who are our Alumni?</p>
<p class="p3">All individuals who have followed Undergraduate(B.Tech), Postgraduate(M.SC,MCA,M.phil,M.Tech) or PhD of the programmes of  CSE,ECE,EEE,CS,EL,CSY for the duration of at least one semester at Sambalpur University Institute of Information Technology as our alumni.</p>
<p class="p2"><br>How do I become a member of the Sambalpur University Institute of Information Technology Alumni?</p>
<p class="p3">All Undergraduate(B.Tech), Postgraduate(M.SC,MCA,M.phil,M.Tech) or PhD  of Sambalpur University Institute of Information Technology can register as our alumni. It is important for alumni to update their particulars for the SUIIT Alumni Directory (Database) by register as a member online and pay at the counter of Alumni SUIIT so that we can keep in touch with you for life.</p>
<p class="p2"><br>Do I need to pay any fee?</p>
<p class="p3">Yes, but it is only for membership registration. You can choose to pay yearly or pay for Life Membership. The fee for yearly paid and life membership are RS300 and RS600 respectively. </p>
<p class="p2"><br>As an alumni, how can I contribute towards the Institute?</p>
<p class="p3">You can help by participating in the various activities organized by the Institute or by contributing ideas or programmes towards enriching learning experience of all students. Other assistance include donation, sponsorship, expertise and volunteer services. Whatever your contribution they are most welcome.</p>
<p class="p2"><br>I am still a student of the Institute, can I take part in alumni activities?</p>
<p class="p3">Yes, of course. We believed that the bond between alumni and students should begin as soon as the student enters the Institute because you will become our future alumni later on. The Alumni Advancement is also committed in organising training especially in soft skills and career development programmes to raise your employability in the job market upon graduation. We hope this will instill a pride in your alma mater and later on may wish to make whatever contribution towards achieving excellence for the Institute.
</p>
